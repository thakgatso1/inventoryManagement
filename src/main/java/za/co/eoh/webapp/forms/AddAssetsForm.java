package za.co.eoh.webapp.forms;

/**
 *
 * @author thakgatso
 */
public class AddAssetsForm {
    private int assetID;
    private String serialNo;
    private String type,make,model,comment,lifeSpan;
    private String purchaseDate;

    public String getSerialNo() {
        return serialNo;
    }

    public void setSerialNo(String serialNo) {
        this.serialNo = serialNo;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getMake() {
        return make;
    }

    public void setMake(String make) {
        this.make = make;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }

    public String getPurchaseDate() {
        return purchaseDate;
    }

    public void setPurchaseDate(String purchaseDate) {
        this.purchaseDate = purchaseDate;
    }

    public String getLifeSpan() {
        return lifeSpan;
    }

    public void setLifeSpan(String lifeSpan) {
        this.lifeSpan = lifeSpan;
    }

    public int getAssetID() {
        return assetID;
    }

    public void setAssetID(int assetID) {
        this.assetID = assetID;
    }

    @Override
    public String toString() {
        return "AddAssetsForm{" + "assetID=" + assetID + ", serialNo=" + serialNo + ", type=" + type + ", make=" + make + ", model=" + model + ", comment=" + comment + ", lifeSpan=" + lifeSpan + ", purchaseDate=" + purchaseDate + '}';
    }
    
}
